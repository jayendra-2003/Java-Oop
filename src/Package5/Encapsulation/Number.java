package Package5.Encapsulation;

public class Number {
    private int a;
    private int b;
    private int c;

    public Number(int a,int b, int c){
        this.a=a;
        this.b=b;
        this.c=c;
    }
    public int getA(){

        return a;
    }
    public void setA(int a){
        this.a=a;
    }
    public int getB(){
        return b;
    }
    public void setB(){
        this.b=b;
    }
    public int getC(){
        return c;
    }
    public void setC(){
        this.c=c;
    }
}
